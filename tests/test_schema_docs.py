# Drift check â€” committed docs must match the generator output
# ---------------------------------------------------------------------------


@pytest.mark.parametrize("table", TABLE_NAMES)
def test_checked_in_doc_matches_generator_output(
    table: str, generator_module: ModuleType
) -> None:
    """The committed ``docs/schemas/<table>.md`` must match the generator.

    If this fails, run::

        python scripts/gen_schema_docs.py

    to refresh the docs and commit the result.
    """
    expected = generator_module.render_table_doc(table)

    doc_path = SCHEMAS_DIR / f"{table}.md"
    assert doc_path.is_file(), (
        f"Missing canonical schema doc: {doc_path}. "
        "Run `python scripts/gen_schema_docs.py` to create it."
    )
    actual = doc_path.read_text(encoding="utf-8")

    assert actual == expected, (
        f"docs/schemas/{table}.md is out of sync with code. "
        "Run `python scripts/gen_schema_docs.py` to refresh."
    )


def test_generator_writes_all_three_tables(
    tmp_path: Path, generator_module: ModuleType
) -> None:
    """Running the generator end-to-end writes all three table docs."""
    generator_module.write_all_schema_docs(tmp_path)

    for table in TABLE_NAMES:
        path = tmp_path / f"{table}.md"
        assert path.is_file(), f"Generator failed to write {path}"
        content = path.read_text(encoding="utf-8")
        assert content.startswith("<!-- AUTOGENERATED")
