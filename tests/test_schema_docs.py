"""Tests for the canonical schema-doc generator.

Ensures ``docs/schemas/{signals,annotations,manifests}.md`` stays in sync
with the authoritative code sources:

- ``signals`` â†’ :class:`agent_diagnostics.types.TrialSignals` TypedDict
- ``annotations`` â†’ :data:`agent_diagnostics.annotation_store.ROW_FIELDS`
- ``manifests`` â†’ keys emitted by
  :func:`agent_diagnostics.export.refresh_manifests`

The drift test regenerates the docs into a temp directory and diffs the
result against the checked-in files. If a contributor adds a column
without regenerating, this test fails with a pointer to the regen
command.
"""

from __future__ import annotations

import importlib.util
import sys
from pathlib import Path
from types import ModuleType

import pytest

REPO_ROOT = Path(__file__).resolve().parents[1]
SCHEMAS_DIR = REPO_ROOT / "docs" / "schemas"
GENERATOR_PATH = REPO_ROOT / "scripts" / "gen_schema_docs.py"

TABLE_NAMES = ("signals", "annotations", "manifests")


@pytest.fixture(scope="session")
def generator_module() -> ModuleType:
    """Dynamically import scripts/gen_schema_docs.py once per test session.

    Session-scoped so the script is loaded and its module-level dataclasses
    constructed exactly once â€” otherwise the 7 tests below would each
    re-exec the file.

    The module is registered in ``sys.modules`` **before** ``exec_module``
    because ``@dataclass(frozen=True)`` (used for ``_Column``) looks up
    ``cls.__module__`` via ``sys.modules`` during class construction. If
    the module is not addressable under its spec name at that point, the
    decorator raises.

    Raises RuntimeError (not ``assert``) on loader failure so the guard
    still protects callers when tests run under ``python -O``.
    """
    spec = importlib.util.spec_from_file_location(
        "gen_schema_docs_test_module", GENERATOR_PATH
    )
    if spec is None or spec.loader is None:
        raise RuntimeError(
            f"Could not load import spec for {GENERATOR_PATH}; "
            "check the file exists and is a regular Python module."
        )
    module = importlib.util.module_from_spec(spec)
    sys.modules[spec.name] = module
    spec.loader.exec_module(module)
    return module


