@pytest.mark.parametrize("table", TABLE_NAMES)
def test_generated_doc_contains_markdown_column_table(
    table: str, generator_module: ModuleType
) -> None:
    """Every table doc must contain a Markdown column table with a header."""
    rendered = generator_module.render_table_doc(table)

    assert rendered.startswith("<!-- AUTOGENERATED"), (
        "Generated doc must begin with an autogenerated banner pointing to the "
        "source file and regen command."
    )
    assert "| Column |" in rendered, "Expected a Markdown column header row"
    assert "| Type |" in rendered, "Expected a Markdown type header row"


def test_signals_doc_covers_every_trial_signals_field(
    generator_module: ModuleType,
) -> None:
    """Every field in TrialSignals must appear in the signals doc.

    Uses ``typing.get_type_hints`` (not ``__annotations__``) so this test
    matches what the generator actually consumes and so TYPE_CHECKING-only
    forward references surface as NameError here rather than silently
    passing a check that the generator would fail.
    """
    import typing

    from agent_diagnostics.types import TrialSignals

    rendered = generator_module.render_table_doc("signals")

    expected = set(typing.get_type_hints(TrialSignals).keys())
    for field in expected:
        assert f"| {field} |" in rendered, (
            f"TrialSignals field {field!r} missing from signals.md "
            f"â€” regenerate docs/schemas/ from code."
        )


def test_annotations_doc_covers_every_row_field(generator_module: ModuleType) -> None:
    """Every AnnotationStore.ROW_FIELDS entry must appear in annotations.md."""
    from agent_diagnostics.annotation_store import ROW_FIELDS

    rendered = generator_module.render_table_doc("annotations")

    for field in ROW_FIELDS:
        assert f"| {field} |" in rendered, (
            f"AnnotationStore field {field!r} missing from annotations.md"
        )


def test_manifests_doc_covers_every_refresh_manifests_key(
    generator_module: ModuleType,
) -> None:
    """Every key in ``_MANIFESTS_DESCRIPTIONS`` must appear in manifests.md.

    Sources the expected key set from the generator's own
    ``_MANIFESTS_DESCRIPTIONS`` rather than a hardcoded tuple. Otherwise
    if ``export.refresh_manifests`` gains a key that the generator's
    descriptions table forgets, a hardcoded list here would silently keep
    passing while the docs drifted out of sync with the producer.
    """
    rendered = generator_module.render_table_doc("manifests")

    for field in generator_module._MANIFESTS_DESCRIPTIONS:
        assert f"| {field} |" in rendered, (
            f"manifests field {field!r} missing from manifests.md"
        )


